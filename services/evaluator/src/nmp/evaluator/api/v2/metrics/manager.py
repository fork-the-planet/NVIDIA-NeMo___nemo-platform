class _PlatformSecretResolver:
    """Resolve evaluator SDK secret refs through the request-scoped platform SDK."""

    def __init__(self, *, workspace: str, sdk: AsyncNeMoPlatform) -> None:
        self._workspace = workspace
        self._sdk = sdk

    async def resolve_secret(self, secret_ref: SecretRef) -> str | None:
        try:
            secret = await self._sdk.secrets.access(secret_ref.root, workspace=self._workspace)
            return secret.value
        except NotFoundError as e:
            raise MetricEvaluationError(
                "SECRET_NOT_FOUND",
                f"Secret '{self._workspace}/{secret_ref.root}' required by metric not found.",
            ) from e


def _append_model_secret(secrets: list[SecretRef | ApiSecretRef], model: object) -> None:
    secret = getattr(model, "api_key_secret", None)
    if isinstance(secret, SecretRef | ApiSecretRef):
        secrets.append(secret)


# Fields generated by the entity store that should be excluded from metric comparisons
_ENTITY_METADATA_FIELDS = {"id", "entity_id", "created_at", "updated_at"}


def _metrics_equal(metric1: BaseModel, metric2: BaseModel) -> bool:
    """Compare two metrics, ignoring database-generated metadata fields."""
    return metric1.model_dump(exclude=_ENTITY_METADATA_FIELDS) == metric2.model_dump(exclude=_ENTITY_METADATA_FIELDS)


def _default_aggregate_fields() -> frozenset[AggregateFieldName]:
    """Return the default aggregate fields with the literal type retained for ty."""
    return cast(frozenset[AggregateFieldName], frozenset(get_args(DefaultAggregateFieldName)))


