    @retry(
        stop=stop_after_attempt(10),
        wait=wait_exponential(multiplier=1, min=4, max=30),
    )
    async def _ensure_system_workspace_exists(self) -> None:
        # Use service principal if configured for startup/background tasks
        _ = await get_async_platform_sdk(as_service=self._as_service, internal=True).workspaces.retrieve(
            SYSTEM_WORKSPACE
        )

    async def delete_all_system_metrics(self) -> None:
        registered_system_metrics_list = await self._get_registered_system_metrics()
        tasks = []
        for metric in registered_system_metrics_list.data:
            if metric.name is None:
                _logger.warning("Skipping system metric deletion because the registered metric has no name")
                continue
            _logger.debug("Deleting system metric from entity service", extra={"metric": metric.name})
            tasks.append(self._entity_client.delete(entities.SystemMetric, metric.name, workspace=SYSTEM_WORKSPACE))
        await asyncio.gather(*tasks)
        _logger.info("Deleted system metrics from the entity service", extra={"count": len(tasks)})

    async def register_system_metrics(self, recreate_existing: bool = False) -> None:
        # TODO: This is a temporary solution to ensure that the system metrics are registered in the entity service.
        # We need to align on the right approach for seed data.
        with scoped_otel_headers(MARK_INTERNAL_REQUEST_HEADERS):
            await self._register_system_metrics_impl(recreate_existing)

    async def _register_system_metrics_impl(self, recreate_existing: bool) -> None:
        await self._ensure_system_workspace_exists()
        if recreate_existing:
            _logger.info("Reregistering system metrics in the entity service...")
            await self.delete_all_system_metrics()
        else:
            _logger.info("Registering system metrics in the entity service...")
        registered_system_metrics_list = await self._get_registered_system_metrics()
        registered_system_metrics = {metric.name: metric for metric in registered_system_metrics_list.data}

        tasks = []
        system_metrics = get_all_system_metrics()
        metrics_warning = []

        # Use service SDK for system metric registration (startup task)
        service_sdk = get_async_platform_sdk(as_service=self._as_service, internal=True)
        for metric in system_metrics:
            metric_entity = entities.SystemMetric(**metric.model_dump(exclude_none=True))
            if metric.name not in registered_system_metrics:
                _logger.debug("Creating system metric in entity service", extra={"metric": metric.name})
                tasks.append(self.create(metric_entity, sdk=service_sdk))
            elif not _metrics_equal(registered_system_metrics[metric.name], metric_entity):
                metrics_warning.append(metric.name)
            else:
                _logger.debug("System metric is up to date in entity service", extra={"metric": metric.name})
        if metrics_warning:
            _logger.warning("System metric is not up to date in entity service", extra={"metrics": metrics_warning})

        await asyncio.gather(*tasks)
        _logger.info("Registered new system metrics in the entity service", extra={"count": len(tasks)})
