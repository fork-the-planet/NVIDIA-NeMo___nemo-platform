BenchmarkServiceError = Literal[
    "BENCHMARK_NOT_FOUND",
    "METRIC_NOT_FOUND",
    "INVALID_METRIC",
    "INVALID_BENCHMARK",
    "BENCHMARK_ALREADY_EXISTS",
    "BENCHMARK_JOB_RESULT_NOT_FOUND",
]

_logger = logging.getLogger(__name__)

# Fields generated by the entity store that should be excluded from metric comparisons
_ENTITY_METADATA_FIELDS = {"id", "entity_id", "created_at", "updated_at"}


class _BenchmarkServiceError(Exception):
    error_code: BenchmarkServiceError
    detail: str

    def __init__(self, error_code: BenchmarkServiceError, detail: str):
        super().__init__(f"{error_code}: {detail}")
        self.error_code = error_code
        self.detail = detail


class BenchmarkRetrievalError(_BenchmarkServiceError): ...


class BenchmarkCreationError(_BenchmarkServiceError): ...


class BenchmarkDeletionError(_BenchmarkServiceError): ...


class BenchmarksManager:
    def __init__(self, entity_client: EntityClient, *, as_service: str | None = None):
        self._entity_client = entity_client
        self._as_service = as_service
        self._mapper = BenchmarkMapper()

    async def get_all(
        self,
        *,
        workspace: str,
        extended_response: bool = False,
        page: int = 1,
        page_size: int = 100,
        sort: Literal["-created_at", "created_at", "-updated_at", "updated_at", "-name", "name"] = "-created_at",
        parsed_filter: ParsedFilter | None = None,
    ) -> ListResponse[Benchmark] | ListResponse[ExtendedBenchmark] | ListResponse[SystemBenchmark]:
        """List all evaluation benchmarks with optional filtering, pagination, and sorting."""
        filter_op = parsed_filter.operation if parsed_filter else None

        if workspace == SYSTEM_WORKSPACE:
            results = await self._entity_client.list(
                entities.SystemBenchmark,
                workspace=workspace,
                sort=sort,
                page=page,
                page_size=page_size,
                filter_operation=filter_op,
            )
            results.data = [self._mapper.entity_to_schema(benchmark, SystemBenchmark) for benchmark in results.data]
            return results

        results = await self._entity_client.list(
            entities.Benchmark,
            workspace=workspace,
            sort=sort,
            page=page,
            page_size=page_size,
            filter_operation=filter_op,
        )
        if extended_response:
            results.data = [self._mapper.entity_to_schema(benchmark, ExtendedBenchmark) for benchmark in results.data]
        else:
            results.data = [self._mapper.entity_to_schema(benchmark, Benchmark) for benchmark in results.data]
        return results

    async def get_by_name(
        self, workspace: str, name: str, *, extended_response: bool = False
    ) -> Benchmark | ExtendedBenchmark | SystemBenchmark | None:
        if workspace == SYSTEM_WORKSPACE:
            try:
                benchmark = await self._entity_client.get(entities.SystemBenchmark, name=name, workspace=workspace)
            except EntityNotFoundError as e:
                raise BenchmarkRetrievalError(
                    "BENCHMARK_NOT_FOUND", f"Benchmark '{workspace}/{name}' not found."
                ) from e
            return self._mapper.entity_to_schema(benchmark, SystemBenchmark) if benchmark else None

        try:
            benchmark = await self._entity_client.get(entities.Benchmark, name=name, workspace=workspace)
        except EntityNotFoundError as e:
            raise BenchmarkRetrievalError("BENCHMARK_NOT_FOUND", f"Benchmark '{workspace}/{name}' not found.") from e
        if extended_response:
            return self._mapper.entity_to_schema(benchmark, ExtendedBenchmark)
        return self._mapper.entity_to_schema(benchmark, Benchmark) if benchmark else None

    async def create(
        self,
        workspace: str,
        benchmark: BenchmarkRequest,
        sdk: AsyncNeMoPlatform,
        *,
        extended_response: bool = False,
    ) -> Benchmark | ExtendedBenchmark:
        metrics = []
        for metric_ref in benchmark.metrics:
            metric_workspace, metric_name = metric_ref.root.split("/")
            if metric_workspace == SYSTEM_WORKSPACE:
                raise BenchmarkCreationError(
                    "INVALID_METRIC",
                    f"Metric in workspace '{SYSTEM_WORKSPACE}' cannot be used for benchmarks at this time.",
                )
            try:
                metric: entities.Metric = await self._entity_client.get(
                    entities.Metric, name=metric_name, workspace=metric_workspace
                )
            except EntityNotFoundError as e:
                raise BenchmarkCreationError(
                    "METRIC_NOT_FOUND", f"Metric '{metric_workspace}/{metric_name}' not found."
                ) from e
            metrics.append(metric)
        creation_check = await benchmark_creation_schema_check(
            benchmark.dataset,
            metrics,
            benchmark.field_mapping,
            sdk,
        )
        if not creation_check.status:
            raise BenchmarkCreationError("INVALID_BENCHMARK", " ".join(creation_check.errors))
        entity = self._mapper.request_to_entity(benchmark, workspace, metrics)
        try:
            entity_response = await self._entity_client.create(entity)
        except EntityConflictError as e:
            raise BenchmarkCreationError(
                "BENCHMARK_ALREADY_EXISTS",
                f"Benchmark '{workspace}/{benchmark.name}' already exists.",
            ) from e
        assert isinstance(entity_response, entities.Benchmark)
        if extended_response:
            return self._mapper.entity_to_schema(entity_response, ExtendedBenchmark)
        return self._mapper.entity_to_schema(entity_response, Benchmark)

    async def delete(self, workspace: str, name: str) -> DeleteResponse:
        try:
            benchmark: entities.Benchmark = await self._entity_client.get(
                entities.Benchmark, name=name, workspace=workspace
            )
        except EntityNotFoundError as e:
            raise BenchmarkDeletionError("BENCHMARK_NOT_FOUND", f"Benchmark '{workspace}/{name}' not found.") from e
        delete_response = await self._entity_client.delete(entities.Benchmark, benchmark.name, workspace=workspace)
        deleted_id = str(delete_response.id) if delete_response.id is not None else None
        deleted_at = delete_response.deleted_at
        if isinstance(deleted_at, str):
            deleted_at = datetime.fromisoformat(deleted_at)
        message = (
            str(delete_response.message) if delete_response.message is not None else "Resource deleted successfully."
        )
        return DeleteResponse(
            message=message,
            id=deleted_id,
            deleted_at=deleted_at,
        )

    async def exists(self, workspace: str, name: str) -> bool:
        try:
            if workspace == SYSTEM_WORKSPACE:
                await self._entity_client.get(entities.SystemBenchmark, name=name, workspace=workspace)
            else:
                await self._entity_client.get(entities.Benchmark, name=name, workspace=workspace)
        except EntityNotFoundError:
            return False
        return True

    async def get_benchmark(self, benchmark: app.BenchmarkRef) -> entities.Benchmark | entities.SystemBenchmark:
        """Get a benchmark entity from a reference.

        Args:
            benchmark: Reference to benchmark (format: workspace/name).

        Returns:
            The benchmark entity.

        Raises:
            BenchmarkRetrievalError: If benchmark does not exist.
        """
        workspace, name = benchmark.root.split("/")
        if workspace == SYSTEM_WORKSPACE:
            try:
                return await self._entity_client.get(entities.SystemBenchmark, name=name, workspace=workspace)
            except EntityNotFoundError as e:
                raise BenchmarkRetrievalError(
                    "BENCHMARK_NOT_FOUND", f"Benchmark '{workspace}/{name}' not found."
                ) from e
        try:
            return await self._entity_client.get(entities.Benchmark, name=name, workspace=workspace)
        except EntityNotFoundError as e:
            raise BenchmarkRetrievalError("BENCHMARK_NOT_FOUND", f"Benchmark '{workspace}/{name}' not found.") from e

    async def compile_job(self, workspace: str, job: BenchmarkJob, sdk: AsyncNeMoPlatform) -> PlatformJobSpec:
        """Compile a benchmark job input to a platform job spec.

        This function:
        1. Retrieves the benchmark entity from the reference
        2. Resolves any ModelRef fields in benchmark_params to Model
        3. Validates prechecks (fileset exists)
        4. Compiles to platform job spec

        Args:
            workspace: The workspace for the job (from API path parameter).
            job: The benchmark job input specification.
            sdk: SDK instance with request-scoped user context. Required and must be obtained
                 from Depends(get_sdk_client) in API endpoints to ensure proper user context.

        Returns:
            Platform job specification ready for execution.

        Raises:
            BenchmarkRetrievalError: If benchmark reference points to a non-existent benchmark.
            ValueError: If prechecks fail or compilation fails.
        """
        # Retrieve benchmark from reference
        benchmark_entity = await self.get_benchmark(job.benchmark)

        # Resolve model refs in benchmark_params (for system benchmarks with judge models)
        if isinstance(job, (SystemBenchmarkOnlineJob, SystemBenchmarkOfflineJob)):
            job.benchmark_params = await resolve_params_model_refs(job.benchmark_params)

        # Run prechecks (fileset exists + model reachability)
        result = await CompositeCheck(
            benchmark_fileset_exists_check(job, benchmark_entity, sdk=sdk),
            benchmark_model_check(job, benchmark_entity, workspace, sdk=sdk),
            benchmark_job_schema_check(job, benchmark_entity, sdk=sdk),
        )()
        if not result.status:
            raise ValueError(f"Job cannot be launched. Error: {str(result)}")

        # Resolve ModelRef to Model before converting to app-layer types.
        # The app layer only accepts Model, so any ModelRef strings must be
        # resolved to concrete model endpoints here in the API layer.
        data = job.model_dump(exclude_none=True, exclude={"benchmark"})
        if isinstance(job, (BenchmarkOnlineJob, SystemBenchmarkOnlineJob)):
            if isinstance(job.model, app.ModelRef):
                data["model_ref"] = job.model
            resolved = await resolve_model(job.model)
            data["model"] = resolved.model_dump()

        # Build the benchmark value for the app-layer discriminated union.
        # Both app.Benchmark and app.SystemBenchmark now have a "name" field;
        # the discriminator distinguishes them by the presence of "metrics".
        # For custom benchmarks, "name" is set to the full ref (workspace/name)
        # so the app layer can identify the benchmark without being workspace-aware.
        # SystemBenchmark (both entities and app layer) don't have a workspace attribute
        if isinstance(benchmark_entity, (entities.SystemBenchmark, app.SystemBenchmark)):
            benchmark = app.SystemBenchmark(**benchmark_entity.model_dump(exclude_none=True))
        else:
            benchmark_ref = f"{benchmark_entity.workspace}/{benchmark_entity.name}"
            benchmark_metrics: list[app.BenchmarkMetric] = []
            for metric in benchmark_entity.metrics:
                benchmark_metrics.append(
                    app.BenchmarkMetric(
                        metric_ref=app.MetricRef(root=f"{metric.workspace}/{metric.name}"),
                        metric=app.MetricAdapter.validate_python(metric.model_dump(exclude_none=True)),
                    )
                )

            benchmark = app.Benchmark(
                name=benchmark_ref,
                description=benchmark_entity.description,
                metrics=benchmark_metrics,
                dataset=benchmark_entity.dataset,
                field_mapping=benchmark_entity.field_mapping,
                labels=benchmark_entity.labels,
            )

        # Convert API schema to BenchmarkJob via discriminated union
        compiled_data = rewrite_models_for_job_container(data | {"benchmark": benchmark})
        benchmark_job = app.BenchmarkJobAdapter.validate_python(compiled_data)

        # Compile to platform job spec
        compiled_job = await compile_benchmark_job(benchmark_job)
        return compiled_job

    # =============================================================================
    # Benchmark Job Results
    # =============================================================================

    async def delete_job_result(self, workspace: str, name: str) -> DeleteResponse:
        try:
            await self._entity_client.get(entities.BenchmarkJobResult, workspace=workspace, name=name)
        except EntityNotFoundError as e:
            raise BenchmarkDeletionError(
                "BENCHMARK_JOB_RESULT_NOT_FOUND", f"Benchmark job result '{workspace}/{name}' not found."
            ) from e
        await self._entity_client.delete(entities.BenchmarkJobResult, name, workspace=workspace)
        return DeleteResponse(
            message="Resource deleted successfully",
            id=f"{workspace}/{name}",
            deleted_at=datetime.now(timezone.utc),
        )

    async def get_job_result(
        self, workspace: str, name: str, aggregate_fields: frozenset[AggregateFieldName] | None = None
    ) -> BenchmarkJobResult:
        """Get a benchmark job result."""
        try:
            entity: entities.BenchmarkJobResult = await self._entity_client.get(
                entities.BenchmarkJobResult, workspace=workspace, name=name
            )
        except EntityNotFoundError as e:
            raise BenchmarkRetrievalError(
                "BENCHMARK_JOB_RESULT_NOT_FOUND", f"Benchmark job result '{workspace}/{name}' not found."
            ) from e

        job_result = self._mapper.entity_to_schema(entity, BenchmarkJobResult)
        if aggregate_fields:
            for result in job_result.results:
                result.scores = [score.with_fields(aggregate_fields) for score in result.scores]
        return job_result

    async def get_job_results(
        self,
        workspace: str,
        aggregate_fields: frozenset[AggregateFieldName] | None = None,
        page: int = 1,
        page_size: int = 100,
        sort: Literal["-created_at", "created_at", "-updated_at", "updated_at", "-name", "name"] = "-created_at",
        parsed_filter: ParsedFilter | None = None,
    ) -> BenchmarkJobResultsListResponse:
        """List benchmark job results with optional filtering, pagination, and sorting."""
        filter_op = parsed_filter.operation if parsed_filter else None

        resp: ListResponse[entities.BenchmarkJobResult] = await self._entity_client.list(
            entities.BenchmarkJobResult,
            workspace=workspace,
            sort=sort,
            page=page,
            page_size=page_size,
            filter_operation=filter_op,
        )

        benchmarks = [self._mapper.entity_to_schema(result, BenchmarkJobResult) for result in resp.data]

        if aggregate_fields:
            for benchmark in benchmarks:
                for result in benchmark.results:
                    result.scores = [score.with_fields(aggregate_fields) for score in result.scores]

        return BenchmarkJobResultsListResponse(
            data=benchmarks,
            pagination=PaginationData(**resp.pagination.model_dump()),
            sort=sort,
            filter=parsed_filter.to_response() if parsed_filter else None,
        )

    # =============================================================================
    # System Benchmarks
    # =============================================================================

    @retry(
        stop=stop_after_attempt(10),
        wait=wait_exponential(multiplier=1, min=4, max=15),
    )
    async def _get_registered_system_benchmarks(self) -> ListResponse[entities.SystemBenchmark]:
        return await self._entity_client.list(
            entities.SystemBenchmark, workspace=SYSTEM_WORKSPACE, page_size=1000
        )  # get all if more than 100

    @retry(
        stop=stop_after_attempt(10),
        wait=wait_exponential(multiplier=1, min=4, max=30),
    )
    async def _ensure_system_workspace_exists(self) -> None:
        # Use service principal if configured for startup/background tasks
        _ = await get_async_platform_sdk(as_service=self._as_service, internal=True).workspaces.retrieve(
            SYSTEM_WORKSPACE
        )

    async def delete_all_system_benchmarks(self) -> None:
        registered_system_benchmarks_list = await self._get_registered_system_benchmarks()
        tasks = []
        for benchmark in registered_system_benchmarks_list.data:
            _logger.debug("Deleting system benchmark from entity service", extra={"benchmark": benchmark.name})
            tasks.append(
                self._entity_client.delete(entities.SystemBenchmark, benchmark.name, workspace=SYSTEM_WORKSPACE)
            )
        await asyncio.gather(*tasks)
        _logger.info("Deleted system benchmarks from the entity service", extra={"count": len(tasks)})

    async def register_system_benchmarks(self, recreate_existing: bool = False) -> None:
        with scoped_otel_headers(MARK_INTERNAL_REQUEST_HEADERS):
            await self._register_system_benchmarks_impl(recreate_existing)

    async def _register_system_benchmarks_impl(self, recreate_existing: bool) -> None:
        await self._ensure_system_workspace_exists()
        if recreate_existing:
            _logger.info("Reregistering system benchmarks in the entity service...")
            await self.delete_all_system_benchmarks()
        else:
            _logger.info("Registering system benchmarks in the entity service...")
        registered_system_benchmarks_list = await self._get_registered_system_benchmarks()
        registered_system_benchmarks = {
            benchmark.name: benchmark for benchmark in registered_system_benchmarks_list.data
        }

        tasks = []
        system_benchmarks = get_all_system_benchmarks()
        benchmarks_warning = []

        for benchmark in system_benchmarks:
            benchmark_entity = entities.SystemBenchmark(**benchmark.model_dump(exclude_none=True))
            if benchmark.name not in registered_system_benchmarks:
                _logger.debug("Creating system benchmark in entity service", extra={"benchmark": benchmark.name})
                tasks.append(self._entity_client.create(benchmark_entity))
            elif not _benchmarks_equal(registered_system_benchmarks[benchmark.name], benchmark_entity):
                benchmarks_warning.append(benchmark.name)
            else:
                _logger.debug("System benchmark is up to date in entity service", extra={"benchmark": benchmark.name})
        if benchmarks_warning:
            _logger.warning(
                "System benchmark is not up to date in entity service", extra={"benchmarks": benchmarks_warning}
            )

        await asyncio.gather(*tasks)
        _logger.info("Registered new system benchmarks in the entity service", extra={"count": len(tasks)})


def _benchmarks_equal(benchmark1: entities.SystemBenchmark, benchmark2: entities.SystemBenchmark) -> bool:
    """Compare two benchmarks, ignoring database-generated metadata fields."""
    return benchmark1.model_dump(exclude=_ENTITY_METADATA_FIELDS) == benchmark2.model_dump(
        exclude=_ENTITY_METADATA_FIELDS
    )
