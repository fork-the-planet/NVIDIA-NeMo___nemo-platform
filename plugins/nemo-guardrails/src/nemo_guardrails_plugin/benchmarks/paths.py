"""Filesystem layout for the nemo-guardrails IGW benchmark harness.

A benchmark invocation produces a `runs/<timestamp>/` directory that holds
everything generated by that run: AIPerf results, supervised-process logs,
and the materialized AIPerf config. Some artifacts are intentionally kept
*outside* the per-run dir so they can be reused across runs (the NMP data
dir, the aiperf venv).

`RunPaths` is the single source of truth for those locations; `build_run_paths`
composes it from the two repo roots the harness needs (NMP and NeMo-Guardrails).
"""

from __future__ import annotations

import datetime as dt
from dataclasses import dataclass
from pathlib import Path


@dataclass(frozen=True)
class RunPaths:
    """Filesystem layout for a single benchmark invocation."""

    # Root of the nemo-platform repo we're running from. Used as the cwd for
    # `nemo services run`.
    nmp_repo_root: Path
    # Root of a local NeMo-Guardrails checkout. The harness reads mock-LLM
    # configs and the content_safety_local example from here.
    nemoguardrails_repo_root: Path
    # Convenience anchor for everything under
    # `plugins/nemo-guardrails/benchmarks/` in the NMP repo.
    benchmark_dir: Path
    # Per-invocation directory under `benchmarks/artifacts/runs/<timestamp>/`.
    # Everything produced by this run lives here so CI artifact upload and
    # local cleanup can treat it as a single unit.
    run_dir: Path
    # Stdout/stderr for every supervised child (mocks, nmp-services, shim,
    # aiperf orchestrator). Lives under `run_dir`.
    log_dir: Path
    # Scratch dir for files the harness materializes at runtime: the
    # rewritten AIPerf config, NMP API request bodies we logged for debugging,
    # etc.
    generated_dir: Path
    # Where AIPerf writes its sweep outputs (CSVs, JSONL records, metadata).
    # The harness rewrites the AIPerf config's `output_base_dir` to point here.
    aiperf_output_dir: Path
    # NMP's persistent data directory (SQLite, Secrets vault, etc.). Lives
    # *outside* `run_dir` and is reused across runs so subsequent benchmarks
    # skip workspace / provider / VM creation work; see the README's Cleanup
    # section. Passed to `nemo services run` via `NMP_DATA_DIR`.
    nmp_data_dir: Path
    # Checked-in YAML template for the AIPerf sweep config.
    config_template: Path
    # In-repo mock-LLM `.env` files. Versioned with the benchmark so mock
    # behavior is independent of the NeMo-Guardrails checkout.
    mock_app_env: Path
    mock_content_safety_env: Path
    # Per-run materialized copy of `config_template` with `output_base_dir`
    # overridden to `aiperf_output_dir`. AIPerf is invoked against this file.
    runtime_config: Path
    # Isolated venv for the upstream `aiperf` CLI. Lives outside `run_dir`
    # (sibling to `nmp_data_dir`) so subsequent local runs reuse the install;
    # see `bootstrap.py` for why we don't share the workspace venv.
    aiperf_venv_dir: Path

    @property
    def run_id(self) -> str:
        return self.run_dir.name

    def ensure_directories(self) -> None:
        for path in (
            self.log_dir,
            self.generated_dir,
            self.aiperf_output_dir,
            self.nmp_data_dir,
        ):
            path.mkdir(parents=True, exist_ok=True)

    def aiperf_output_dir_for(self, variant: str) -> Path:
        """Per-variant AIPerf output dir; keeps side-by-side sweeps from colliding."""
        return self.aiperf_output_dir / variant

    def runtime_config_for(self, variant: str) -> Path:
        """Per-variant materialized AIPerf config under ``generated/``."""
        return self.generated_dir / f"aiperf_config_{variant}.yaml"

    def aiperf_log_for(self, variant: str) -> Path:
        """Per-variant AIPerf stdout/stderr log under ``logs/``."""
        return self.log_dir / f"aiperf_{variant}.log"


def _now_run_id() -> str:
    return dt.datetime.now().strftime("%Y%m%d_%H%M%S")


def discover_nmp_repo_root(start: Path | None = None) -> Path:
    """Walk up from ``start`` until a directory containing pyproject.toml + plugins/ is found."""
    candidate = (start or Path(__file__)).resolve()
    for parent in (candidate, *candidate.parents):
        if (parent / "pyproject.toml").is_file() and (parent / "plugins").is_dir():
            return parent
    raise RuntimeError(f"Could not locate NMP repo root from {start or Path(__file__)}")


def default_nemoguardrails_repo_root(nmp_repo_root: Path) -> Path:
    """Default to a sibling ``NeMo-Guardrails`` checkout next to ``nmp_repo_root``."""
    return (nmp_repo_root.parent / "NeMo-Guardrails").resolve()


def build_run_paths(
    *,
    nmp_repo_root: Path,
    nemoguardrails_repo_root: Path,
    run_id: str | None = None,
) -> RunPaths:
    """Compose the benchmark filesystem layout under the plugin's artifacts dir.

    ``run_id`` overrides the per-run directory name (default: current timestamp).
    """
    benchmark_dir = nmp_repo_root / "plugins" / "nemo-guardrails" / "benchmarks"
    artifacts_dir = benchmark_dir / "artifacts"
    run_dir = artifacts_dir / "runs" / (run_id or _now_run_id())

    return RunPaths(
        nmp_repo_root=nmp_repo_root,
        nemoguardrails_repo_root=nemoguardrails_repo_root,
        benchmark_dir=benchmark_dir,
        run_dir=run_dir,
        log_dir=run_dir / "logs",
        generated_dir=run_dir / "generated",
        aiperf_output_dir=run_dir / "aiperf_results",
        nmp_data_dir=artifacts_dir / "nmp-data",
        config_template=benchmark_dir / "configs" / "nmp_igw_guardrails_sweep_concurrency.yaml",
        runtime_config=run_dir / "generated" / "nmp_igw_guardrails_sweep_concurrency.yaml",
        aiperf_venv_dir=artifacts_dir / "venvs" / "aiperf",
        mock_app_env=benchmark_dir / "configs" / "mock_llm" / "app-llm.env",
        mock_content_safety_env=benchmark_dir / "configs" / "mock_llm" / "content-safety-llm.env",
    )
