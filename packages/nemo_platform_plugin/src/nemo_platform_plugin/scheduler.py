@contextmanager
def _local_runtime_env(ctx: JobContext):
    """Mirror the in-container runtime envvars onto the process during ``run``.

    Legacy task code (and the existing per-service compilers) reads
    ``NEMO_JOB_PERSISTENT_JOB_STORAGE_PATH`` and friends via ``os.environ``
    to locate scratch / persistent volumes. Inside a real job pod the Jobs
    controller injects them; in local mode the scheduler synthesizes a
    :class:`JobContext` with tempdir-backed storage and exposes the
    same envvars here so unmodified task code keeps working.

    Caller-set storage values win — we only fill storage envvars that aren't
    already set. The workspace envvar always reflects the current local
    context so a parent job's workspace does not leak into nested local runs.
    Prior environment values are restored on exit so tests and back-to-back
    calls don't leak state.
    """
    storage_overrides: dict[str, str] = {
        _PERSISTENT_STORAGE_ENVVAR: str(ctx.storage.persistent),
        _EPHEMERAL_STORAGE_ENVVAR: str(ctx.storage.ephemeral),
    }
    # Scheduler-built local contexts leave job_id as ``None``. Explicit
    # caller-provided contexts may still mirror a job id for legacy code.
    force_overrides: dict[str, str] = {_WORKSPACE_ENVVAR: ctx.workspace}
    if ctx.job_id is not None:
        force_overrides[_JOB_ID_ENVVAR] = ctx.job_id
    saved: dict[str, str | None] = {}
    try:
        for key, value in storage_overrides.items():
            if key in os.environ:
                continue  # respect explicit caller setup
            saved[key] = None
            os.environ[key] = value
        for key, value in force_overrides.items():
            saved[key] = os.environ.get(key)
            os.environ[key] = value
        yield
    finally:
        for key, prior in saved.items():
            if prior is None:
                os.environ.pop(key, None)
            else:
                os.environ[key] = prior


def _job_key_for(job_cls: type["NemoJob"]) -> str:
    """Best-effort entry-point key for *job_cls*.

    Uses the API segment (top-level package, ``nemo_`` prefix stripped, hyphenated)
    as a fallback when no registered entry point is available — e.g. a job in
    ``nemo_data_designer.jobs.generate`` keys as ``data-designer.<job.name>``,
    matching the documented ``<plugin>.<job>`` convention. Scheduler callers
    that have the registered key should pass it explicitly to
    :meth:`NemoJobScheduler.run_local` via a custom ``ctx``.
    """
    return f"{_api_segment_for(job_cls)}.{job_cls.name}"


def submit_path_for(job_cls: type["NemoJob"], *, workspace: str) -> str:
    """The submit URL path (no host / scheme) for *job_cls*.

    Useful for OpenAPI path lookups and explain bundles; mirrors
    :meth:`NemoJobScheduler._build_submit_url` without the host.
    """
    return f"/apis/{_api_segment_for(job_cls)}/v2/workspaces/{workspace}{job_collection_path_for(job_cls)}"


def _api_segment_for(job_cls: type["NemoJob"]) -> str:
    """Derive the ``{api}`` segment of the submit URL for *job_cls*.

    Jobs register under the ``nemo.jobs`` entry-point group keyed as
    ``<plugin>.<job>``, and the platform mounts their routes under that
    ``<plugin>`` segment. The authoritative source of truth is therefore
    the entry-point key — not the Python module path. We resolve it via
    :func:`~nemo_platform_plugin.discovery.discover_jobs` so a plugin registered
    as ``agents.evaluate`` correctly maps to ``/apis/agents/...``, even
    though its package directory is ``nemo_agents_plugin/`` (which the
    module-name heuristic would have collapsed to ``agents-plugin`` and
    404'd against).

    When the job class isn't installed as an entry point — unit tests
    with inline classes, ad-hoc invocations from a checkout — fall back
    to deriving the segment from the top-level package name with the
    ``nemo_`` prefix stripped and underscores converted to dashes.
    The fallback does *not* strip a trailing ``_plugin``: a plugin whose
    actual key happens to be ``my-plugin`` would be silently rewritten
    to ``my``.
    """
    from nemo_platform_plugin.discovery import discover_jobs

    try:
        registered = discover_jobs()
    except Exception:
        registered = {}
    for key, registered_cls in registered.items():
        if registered_cls is job_cls and "." in key:
            return key.split(".", 1)[0]

    module = job_cls.__module__.split(".")[0]
    if module.startswith("nemo_"):
        module = module[len("nemo_") :]
    return module.replace("_", "-")


def _ensure_local_logging_configured() -> None:
    """Attach a stderr INFO handler to the root logger if none is set.

    No-op when the caller already configured logging (CLI with ``-v``,
    service ``dictConfig``, test harness). Noisy transport libraries
    (``httpx``, ``httpcore``) are clamped to WARNING so per-request
    debug output doesn't drown the job's own messages.
    """
    root = logging.getLogger()
    if root.handlers:
        return
    handler = logging.StreamHandler()
    handler.setFormatter(logging.Formatter("%(levelname)s %(message)s"))
    root.addHandler(handler)
    root.setLevel(logging.INFO)
    logging.getLogger("httpx").setLevel(logging.WARNING)
    logging.getLogger("httpcore").setLevel(logging.WARNING)
